function uuid(index: number): string {
  return `00000000-0000-4000-a000-${String(index).padStart(12, '0')}`;
}

function daysAgo(n: number): string {
  const d = new Date();
  d.setDate(d.getDate() - n);
  return d.toISOString();
}

